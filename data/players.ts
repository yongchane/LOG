// 모든 선수를 하나의 배열로 (확장 데이터 포함)
export const getAllPlayers = (): Player[] => {
  return EXTENDED_PLAYERS;
};

// 포지션별로 선수 가져오기 (확장 데이터 사용)
export const getPlayersByPosition = (position: Position): Player[] => {
  return EXTENDED_PLAYERS.filter((p) => p.position === position);
};

// 랜덤 선수 뽑기 (중복 방지, 확장 데이터 사용)
export const getRandomPlayer = (
  position: Position,
  excludeIds: string[] = [],
): Player => {
  const players = EXTENDED_PLAYERS.filter(
    (p) => p.position === position && !excludeIds.includes(p.id),
  );
  const randomIndex = Math.floor(Math.random() * players.length);
  return players[randomIndex];
};
